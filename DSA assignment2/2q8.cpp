#include <iostream>
using namespace std;

int main() {
    int n;
    cout << "Enter size of array: ";
    cin >> n;

    int arr[n];
    cout << "Enter elements: ";
    for (int i = 0; i < n; i++) cin >> arr[i];

    for (int i = 0; i < n-1; i++) {
        for (int j = 0; j < n-i-1; j++) {
            if (arr[j] > arr[j+1]) {
                int temp = arr[j];
                arr[j] = arr[j+1];
                arr[j+1] = temp;
            }
        }
    }

    int distinctCount = 1; 
    for (int i = 1; i < n; i++) {
        if (arr[i] != arr[i-1]) distinctCount++;
    }

    cout << "Total number of distinct elements: " << distinctCount << endl;
    return 0;
}
